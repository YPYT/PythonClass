### --- Function (for reusability)
## Concept: 1. define function
##          2. call function

def greeting(name):
    print("Hello, " + name)

greeting("Emily")
greeting("Jessie")
greeting("Lisa")

def addition(num1, num2):
    total = num1 + num2
    print("The total is:", total)

addition(3,8)
addition(2872, 1762583)
addition(1.4, 2.4)

def my_function():
    print("This is my function")
def main():
    my_function()
main()


    